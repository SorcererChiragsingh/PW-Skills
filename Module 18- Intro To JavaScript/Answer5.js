// Ascending Order (1 to 25):
let i = 1;
while (i <= 25) 
    {
        process.stdout.write(i + " ");
        i++;
    }
    
// Move to the next line after ascending order is printed
console.log("");

// Descending Order (25 to 1):
let j = 25;
while (j >= 1) 
    {
        process.stdout.write(j + " ");
        j--;
    }
